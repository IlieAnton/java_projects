package Materie20082022.For;

import java.util.Scanner;

public class Forex1 {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        int nr = scanner.nextInt();
        for (int a = 0; a <= nr; a = a + 1) {
            System.out.println(a);
        }
    }
}
