package Materie20082022.For;

public class Forex3 {
    public static void main(String[] args) {
        String propozitie = "ANA ARE MERE";
        int a = propozitie.length()-1;
        System.out.println(a);
        for (int a1 = 0; a1 <= a; a1=a1+1) {
            System.out.println(propozitie.charAt(a1));
        }
    }
}