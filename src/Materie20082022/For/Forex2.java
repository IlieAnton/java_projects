package Materie20082022.For;

import java.util.Scanner;

public class Forex2 {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        int nr = scanner.nextInt();
        for (int a = nr; a >= 1; a=a-1) {
            System.out.println(a);
        }
    }
}
