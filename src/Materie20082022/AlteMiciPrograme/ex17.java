package Materie20082022.AlteMiciPrograme;

public class ex17 {
    public static void main(String[] args) {
        int a = 3;
        int b = 4;
        char c = '-';
        if (c =='+') {
            System.out.println(a + b);
        }else if (c == '-'){
            System.out.println(b - a);
        }else if (c == '*'){
            System.out.println(a * b);
        }else if (c == '/'){
            System.out.println(a / b);
        }
    }
}
