package Materie20082022.Recapitulare;

public class Tara {
    String nume;
    String tricolor;
    int populatie;
    String presedinte;
    int anFondata;

    public Tara (String nume, String tricolor, int populatie, String presedinte, int anFondata) {
        this.nume = nume;
        this.tricolor = tricolor;
        this.populatie = populatie;
        this.presedinte = presedinte;
        this.anFondata = anFondata;
    }
}
