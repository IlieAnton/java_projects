package Materie20082022.Stringrecapitulare;

public class startsWith {
    public static void main(String[] args) {
        String a = "ana are mere";
        if (a.startsWith("A")) {
            System.out.println("Da, incepe cu A");
        } else {
            System.out.println("Nu, nu incepe cu A");
        }
    }
}
